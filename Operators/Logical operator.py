# Logical AND
x = 5
y = 10
z = 15

# Check if both conditions are true
if x < y and y < z:
    print("Both conditions are true (x < y and y < z)")

# Logical OR
if x > y or y < z:
    print("At least one condition is true (x > y or y < z)")

# Logical NOT
is_raining = True

# Negate the boolean value
if not is_raining:
    print("It's not raining")
else:
    print("It is raining")
